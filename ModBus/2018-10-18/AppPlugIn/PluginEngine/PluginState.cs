using System;
namespace Ksat.AppPlugIn.PluginEngine
{
    /// <summary>
    /// 服务运行状态
    /// </summary>
    [System.Runtime.InteropServices.Guid("755CE2B6-5846-4751-A5B5-BAD575860AE1")]
    [System.Runtime.InteropServices.ComVisible(true)]
    public enum PluginState : int
    {
        /// <summary>
        /// 未安装
        /// </summary>
        NotInstalled = 0,
        /// <summary>
        /// 正在运行
        /// </summary>
        Running,
        /// <summary>
        /// 已禁用
        /// </summary>
        Disabled,
        /// <summary>
        /// 加载错误
        /// </summary>
        LoadingError,
        /// <summary>
        /// 未初始化
        /// </summary>
        UnInitialized,
        /// <summary>
        /// 已卸载
        /// </summary>
        Unload,
        /// <summary>
        /// 类型已加载,但是未初始化
        /// </summary>
        TypeLoaded
    }
}
